# Strings são textos que estão dentro de aspas

print(1234)

# Aspas simples
print('Hello, World!')
print(1, 'Hello, World! "Olá, Mundo!"')

# Aspas duplas
print("Hello, World!")
print(2, "Hello, World! 'Olá, Mundo!'")

# Escape
print('Hello, World! \"Olá, Mundo!\"')

# r
print(r'Hello, World! \"Olá, Mundo!\"')
